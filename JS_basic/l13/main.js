/**
 * && - and
 * || - or
 * ! - Not
 */

var a = 1;
var b = 2;
var c = 3;

// &&
if (a > 0 && b > 0 && c != 3) {
    console.log('Right condition')
}

// ||
if (a > 0 || b < 0 || c < 0) {
    console.log('Right condition 2')
}

// !
if (!(a < 0)) {
    console.log('Right condition 3')
}

